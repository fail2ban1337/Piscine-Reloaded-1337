int		ft_count_if(char **tab, int (*f)(char*))
{
	int a;
	int i;

	a = 0;
	i = 0;
	while (tab[a] != 0)
	{
		if (f(tab[a]) == 1)
			i++;
		a++;
	}
	return (i);
}
